  censustract_files <- censustract_files[!grepl("simplified", censustract_files)]
  
  # creates neighborhoods by merging census tracts and saves files

  for (i in censustract_files){  # i = censustract_files[1]
  
    temp <- arrow::open_dataset(i) |> 
      dplyr::filter(!is.na(code_neighborhood)) |> 
      sf::st_as_sf()
    
    ## 0. Create folder to save clean data
    yyyy <- temp$year[1]
