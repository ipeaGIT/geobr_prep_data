# Helper: Clean 2022 data (direct GPKG from IBGE)

clean_neighborhoods_2022 <- function(raw) {

  temp_sf <- raw |>
    dplyr::select(
      code_neighborhood  = CD_BAIRRO,
      name_neighborhood  = NM_BAIRRO,
      code_district      = CD_DIST,
      name_district      = NM_DIST,
      code_subdistrict   = CD_SUBDIST,
      name_subdistrict   = NM_SUBDIST,
      code_muni          = CD_MUN,
      name_muni          = NM_MUN,
      code_state         = CD_UF
    )

  # Remove lakes (administrative areas, not real neighborhoods)
  temp_sf <- dplyr::filter(temp_sf,
                           code_muni != 430000100000000,
                           code_muni != 430000200000000)


  return(temp_sf)
}


# ==============================================================================
# Helper: Clean 2010 data (dissolve from census tracts)
# 3-part approach: neighborhood > subdistrict > district
# ==============================================================================
clean_neighborhoods_2010 <- function(tracts) {

  # Ensure UTF-8 and MULTIPOLYGON
  tracts <- use_encoding_utf8(tracts)
  tracts <- sf::st_cast(tracts, "MULTIPOLYGON")

  ## Part 1: municipalities with neighborhood info ----------------------------
  nei <- dplyr::filter(tracts, !is.na(name_neighborhood))
  all_muni_p1 <- unique(nei$code_muni)

  part1 <- dissolve_by_group(nei, "code_neighborhood")

  # Recover metadata
  meta1 <- sf::st_drop_geometry(nei) |>
    dplyr::select(code_muni, name_muni, name_neighborhood, code_neighborhood,
                  code_subdistrict, name_subdistrict, code_district, name_district) |>
    dplyr::distinct()
  part1 <- dplyr::left_join(part1, meta1, by = "code_neighborhood")

  # Remove municipalities with single observation (covers whole municipality)
  part1 <- part1 |>
    dplyr::group_by(code_muni) |>
    dplyr::filter(dplyr::n() > 1) |>
    dplyr::ungroup()

  ## Part 2: municipalities with subdistrict info only ------------------------
  sub <- dplyr::filter(tracts, is.na(name_neighborhood), !is.na(name_subdistrict))
  sub <- dplyr::filter(sub, !(code_muni %in% all_muni_p1))
  all_muni_p2 <- unique(sub$code_muni)

  part2 <- dissolve_by_group(sub, "code_subdistrict")

  meta2 <- sf::st_drop_geometry(sub) |>
    dplyr::select(code_muni, name_muni, code_subdistrict, name_subdistrict,
                  code_district, name_district) |>
    dplyr::distinct()
  part2 <- dplyr::left_join(part2, meta2, by = "code_subdistrict")
  part2$name_neighborhood <- NA_character_
  part2$code_neighborhood <- NA_real_

  # Remove municipalities with few observations
  part2 <- part2 |>
    dplyr::group_by(code_muni) |>
    dplyr::filter(dplyr::n() > 5) |>
    dplyr::ungroup()

  ## Part 3: municipalities with district info only ---------------------------
  dist <- dplyr::filter(tracts, is.na(name_neighborhood), is.na(name_subdistrict),
                         !is.na(name_district))
  dist <- dplyr::filter(dist, !(code_muni %in% all_muni_p1))
  dist <- dplyr::filter(dist, !(code_muni %in% all_muni_p2))

  part3 <- dissolve_by_group(dist, "code_district")

  meta3 <- sf::st_drop_geometry(dist) |>
    dplyr::select(code_muni, name_muni, code_district, name_district) |>
    dplyr::distinct()
  part3 <- dplyr::left_join(part3, meta3, by = "code_district")
  part3$name_neighborhood <- NA_character_
  part3$code_neighborhood <- NA_real_
  part3$code_subdistrict  <- NA_real_
  part3$name_subdistrict  <- NA_character_

  # Remove municipalities with few observations
  part3 <- part3 |>
    dplyr::group_by(code_muni) |>
    dplyr::filter(dplyr::n() > 15) |>
    dplyr::ungroup()

  ## Union all parts -----------------------------------------------------------
  cols <- c("code_neighborhood", "name_neighborhood",
            "code_district", "name_district",
            "code_subdistrict", "name_subdistrict",
            "code_muni", "name_muni")

  part1 <- dplyr::select(part1, dplyr::all_of(cols))
  part2 <- dplyr::select(part2, dplyr::all_of(cols))
  part3 <- dplyr::select(part3, dplyr::all_of(cols))

  temp_sf <- rbind(part1, part2, part3)

  # Final filter: remove municipalities with single observation
  temp_sf <- temp_sf |>
    dplyr::group_by(code_muni) |>
    dplyr::filter(dplyr::n() > 1) |>
    dplyr::ungroup()

  # Derive code_state from code_muni
  temp_sf$code_state <- substr(as.character(temp_sf$code_muni), 1, 2) |> as.numeric()

  return(temp_sf)
}


# ==============================================================================
# Helper: Dissolve polygons by group column (for 2010 census tract approach)
# Uses dissolve_polygons() from support_harmonize_geobr.R per municipality
# ==============================================================================
dissolve_by_group <- function(sf_data, group_column) {
  all_munis <- unique(sf_data$code_muni)

  result_list <- pbapply::pblapply(all_munis, function(m) {
    tryCatch({
      dissolve_polygons(
        mysf = dplyr::filter(sf_data, code_muni == m),
        group_column = group_column
      )
    }, error = function(e) {
      warning(paste("dissolve_polygons failed for muni", m, ":", conditionMessage(e)))
      NULL
    })
  })

  result_list <- Filter(Negate(is.null), result_list)
  do.call(rbind, result_list)
}
